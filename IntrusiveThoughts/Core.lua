function VoidWhisper(category)
    local currentTime = GetTime()
    local timeSinceLast = currentTime - lastWhisperTime
    
    -- Urgent things (Death, AFK) still bypass the long throttle, but have a short check
    local isUrgent = (category == "PLAYER_DEAD" or category == "LOW_HEALTH" or category == "AFK" or category == "QUEUE_POP" or category == "READY_CHECK" or category == "RESURRECT" or category == "DUEL")
    
    if isUrgent then
        if timeSinceLast < 10 then return end -- Don't spam even on death
    else
        if timeSinceLast < THROTTLE_TIME then return end

        local chance = BASE_CHANCE 
        
        -- ONLY Non-Combat "Flavor" events get a higher chance now.
        -- I REMOVED "SELF_" from here so your rotation doesn't spam you.
        if category == "FLIGHT_MASTER" or category == "AUCTION_HOUSE" or category == "BANK" or 
           category == "MAILBOX" or category == "GENERIC_DUNGEON" or category == "GENERIC_RAID" or 
           category == "VANITY" or category == "CRAFTING" or category == "SUMMON" or category == "MAP" or
           category == "SYSTEM_MENU" or category == "ACHIEVEMENT_FRAME" or
           string.find(category, "ELITE_") then 
            chance = 0.25 -- 25% chance for these rare UI interactions
        end

        if math.random() > chance then return end 
    end

    local table = reactions[category]
    if table then
        -- Pick a random message
        local msg = table[math.random(#table)]
        
        -- Pick a random Entity Entry (which contains {Name, Color})
        local entityEntry = ENTITIES[math.random(#ENTITIES)]
        local entityName = entityEntry[1]
        local entityColor = entityEntry[2]

        -- Print using the specific color for that entity
        print(entityColor .. entityName .. "|r " .. TEXT_COLOR .. "whispers: " .. msg .. "|r")
        
        PlaySound(3081, "Master") 
        lastWhisperTime = currentTime
    end
end

